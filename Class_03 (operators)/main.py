num1 : int = 5
num2 : int = 8


addition : int = num1 + num2 
subtraction : int = num1 - num2
multiplication : int = num1 * num2
division : float = num1 / num2

print(f"5 + 8 = {addition}") # 5 + 8 = 13
print(f"5 - 8 = {subtraction}") # 5 - 8 = -3
print(f"5 * 8 = {multiplication}") # 5 * 8 = 40
print(f"5 / 8 = {division}") # 5 / 8 = 0.625